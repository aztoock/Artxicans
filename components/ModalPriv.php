      El presente aviso de privacidad describe e informa como Artxicans recaba, usa y revela cierta información obtenida a través del sitio www.artxicans.com  de manera personal, telefónica, por medios electrónicos o de correspondencia. En algunos casos, Artxicans podrá obtener dicha información y datos a través de otras fuentes o de terceros.<br>

La información aquí contenida es emitida de conformidad con Ley Federal de Protección de Datos Personales en Posesión de los Particulares (la “Ley”); el Reglamento de la Ley Federal de Protección de Datos Personales en Posesión de los Particulares (el “Reglamento”); los Lineamientos del Aviso de Privacidad (los “Lineamientos”) y demás disposiciones aplicables y vigentes.<br><br>

1. Alcance.<br>

La presente política se aplica a cualquier persona que visite nuestro Sitio y/o adquiera nuestros productos, así como a cualquier persona que contacte a Artxicans o ponga a su disposición sus datos personales de cualquier forma y por cualquier medio, siempre y cuando dicha acción no esté sujeta a otra política de privacidad.<br>

Se entenderá por “dato de carácter personal” cualquier información que se recabe acerca de personas físicas identificadas o identificables; y por “titular”, a cualquier persona física identificada o identificable que comunique sus datos a Artxicans por cualquier medio.<br>

Es responsabilidad del titular de los datos personales, garantizar que los datos proporcionados voluntariamente a Artxicans de manera verbal, documental o electrónica, sean correctos, veraces y completos, debiendo informar oportunamente a Artxicans sobre cualquier modificación, a través de los medios señalados en el presente aviso, con el fin de mantener la información actualizada.<br><br>

2. ¿Qué información recabamos?<br>

Artxicans podrá recabar y tratar información personal de contacto, académica, laboral, incluyendo nombre completo, edad, fecha, correo electrónico, teléfono, domicilio, identificación oficial,  trayectoria laboral o profesional.<br><br>

3. ¿Cómo recabamos tu información?<br>

Artxicans podrá recopilar tu información a través de cualquiera de las siguientes formas:<br>

Cuando proporcionas directamente tu información a Artxicans, ya sea de forma personal, cuando realizarás alguna compra y cuando realizarás un registro para poder ser un vendedor en Artxicans.<br><br>

4. ¿Para qué fines utilizamos tus datos personales?<br>

Artxicans podrá acceder y utilizar tus datos personales para las siguientes finalidades: (i) Identificación y contacto; (ii) Mejorar la experiencia de usuario en el Sitio; (iii) Cualquier otra acción necesaria para cumplir los intereses de Artxicans respecto a la relación con el Titular; (iv) Solicitudes de vendedor. (v) Para realizar correctamente tu envio.<br><br>

5. ¿Cómo protegemos y conservamos tu información?<br>

Tus datos personales son resguardados en base a los principios de licitud, consentimiento, información, calidad, finalidad, lealtad, proporcionalidad y responsabilidad, mismos que se encuentran estipulados en la Ley.<br>

En Artxicans tenemos implementadas políticas y mecanismos que protegen de forma administrativa, técnica y electrónica los datos personales que nos ha proporcionado con el fin de poder evitar pérdidas, usos incorrectos o accesos no autorizados, publicaciones, modificaciones o destrucciones de los datos personales. Estas prácticas de privacidad son llevadas para otorgarle la debida protección, privacidad y confidencialidad que los datos personales merecen. No obstante, Artxicans no garantiza que, por fallas de hardware o software, o por otras circunstancias fuera de su control, terceros accedan o hagan uso no autorizado de dichos datos.<br>

Artxicans conservará tu información mientras tu relación de negocios con nosotros siga activa, salvo que solicites la eliminación de tu información. En algunos supuestos, es posible que se conserve determinada información en nuestra base de datos, hasta en tanto no se extingan las acciones legales que pudieran derivarse de nuestra relación jurídica, caso en el que serán conservados exclusivamente para efectos de las responsabilidades emanadas de su tratamiento o según lo exija la Ley.<br><br>

6. ¿Cuáles son los medios para ejercer los derechos de acceso, rectificación, cancelación u oposición (“ARCO”)?<br>

Queremos que puedas decidir libremente que información compartes con nosotros, para lo cual y de conformidad con lo dispuesto en la Ley, podrás acceder, rectificar y cancelar tus datos personales, así como oponerse a la divulgación de estos a través de los procedimientos que Artxicans ha implementado a través de nuestro Departamento de Protección de Datos Personales.<br>

Para mayor comprensión, a continuación explicamos los derechos ARCO:<br>

● Acceso: Tienes derecho a conocer qué datos personales tenemos de ti, para qué los utilizamos, así como las generalidades de dicho tratamiento.<br>

● Rectificación: Es tu derecho solicitar la corrección de tu información personal en caso de que sea incorrecta, esté desactualizada o incompleta.<br>

● Cancelación: Tienes derecho a que eliminemos tu información de nuestros registros o bases de datos cuando considere que la misma no está siendo utilizada conforme a los principios, deberes y obligaciones previstas en la legislación aplicable.<br>

● Oposición: Puedes oponerte o solicitar el cese del uso de tus datos personales para fines específicos.<br>

Para el ejercicio de cualquiera de los derechos ARCO o la revocación del consentimiento que para tales efectos nos hayas otorgado, deberás presentar una solicitud por escrito dirigida al Departamento de Protección de Datos Personales, al siguiente correo electrónico: said557@outlook.es en el que atenderemos y resolveremos tu solicitud conforme a lo establecido en la Ley. La solicitud ARCO que presentes deberá contener y acompañar al menos lo siguiente, tal y como lo establece el artículo 29 de la Ley:<br>

● Nombre y domicilio del titular, u otro medio para comunicarte la respuesta a tu solicitud;<br>

● Los documentos que acrediten tu identidad o en su caso, la del representante legal del titular;<br>

● La descripción clara y precisa de los datos personales respecto de los que se busca ejercer alguno de los derechos (motivo de la solicitud);<br>

● En el caso de rectificación de alguno de tus datos personales, deberás señalar las modificaciones a realizarse y aportar la documentación que sustente tu petición;<br>

● Cualquier otro elemento o documento que facilite la localización de los datos personales, así como cualquier otro documento que exija la Ley al momento de la presentación de la solicitud.<br>

En caso de que un tercero presente dicho escrito por ti, deberá acreditar la legal representación mediante carta poder firmada ante dos testigos o instrumento público, debiendo anexar identificación oficial vigente del titular de los datos personales y del representante legal.<br>

Artxicans comunicará al titular la determinación adoptada en un plazo máximo de 20 (veinte) días hábiles, contados desde la fecha en que se recibió la solicitud, a efecto de que, si resulta procedente, se haga efectiva la misma dentro de los 15 (quince) días hábiles siguientes a la fecha en que se comunique la respuesta.<br>

Artxicans podrá negar el ejercicio de los derechos ARCO, en los siguientes supuestos:<br>

● Cuando no seas el titular de los datos personales o bien, no hayas acreditado debidamente la representación del titular;<br>

● Cuando tus datos personales no obren en nuestra base de datos;<br>

● Cuando se lesionen los derechos de un tercero;<br>

● Cuando exista un impedimento legal o la resolución de una autoridad competente que restrinja tus derechos ARCO;<br>

● Cuando la rectificación, cancelación u oposición haya sido previamente realizada.<br>

La respuesta podría ser negativa parcialmente, en cuyo caso, Artxicans atenderá la solicitud en la parte procedente. No en todos los casos podremos atender tu solicitud o concluir el uso de tu información de forma inmediata, ya que es posible que por alguna obligación legal requiramos seguir tratando tus datos personales. Asimismo, deberás considerar que para ciertos fines, la revocación de tu consentimiento implicará la conclusión de tu relación con nosotros.<br>

Podrás registrarte en el listado de exclusión, a fin de que tus datos personales no sean tratados para las finalidades secundarias, fines mercadotécnicos, publicitarios o de prospección comercial por nuestra parte, mediante un correo electrónico a la siguiente dirección said557@outlook.es<br><br>


7. Actualizaciones al Aviso de Privacidad.<br>

Artxicans se reserva el derecho de modificar y/o actualizar en cualquier momento el presente aviso de privacidad, con la finalidad de atender novedades legislativas, jurisprudenciales, políticas internas, así como nuevos requerimientos para el adecuado funcionamiento del Sitio y comercialización de nuestros servicios.<br>

Si realizamos cambios significativos, te notificaremos los cambios a través del Sitio, o bien, a través del correo electrónico. En la medida en que la Ley lo permita, al continuar contratando nuestros servicios o visitando el Sitio después de dicha notificación, se entiende que has aceptado las actualizaciones del aviso de privacidad. Te recomendamos que revises periódicamente el presente aviso de privacidad para obtener la información más reciente sobre nuestras políticas.<br><br>

8. Aceptación.<br>

Todo tratamiento de tus datos personales está sujeto a tu consentimiento como titular, salvo las excepciones establecidas en la Ley y su Reglamento. De conformidad con el artículo 8 de la Ley, por el simple hecho de no manifestar oposición al leer el presente aviso de privacidad se tiene por consentido tácitamente el tratamiento de tus datos personales en la totalidad de los términos de éste, salvo aquellos que requieran tu consentimiento expreso en términos de la Ley.<br>

Al compartir tu información con nosotros, visitar nuestro Sitio y/o contratar nuestros servicios, se entiende que has revisado, leído y aceptado los términos de este aviso de privacidad. Asimismo, manifiestas que los datos personales que proporciones a Artxicans han sido obtenidos de manera libre, informada, voluntaria e inequívoca, y además declaras eres mayor de edad.<br><br>

9. INAI.<br>

